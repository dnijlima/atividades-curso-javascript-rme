//Igual a == (true or false) - if
console.log(10 == 10); // true => vdd
console.log(12 == 10); //false => falso

// Diferente de  !=
console.log(10 != 10); // false
console.log(10 != 9); // true

//Maior que >
console.log(10 > 9); //true;
console.log(9 > 10); //false

//Menor que <
console.log(10 < 9); //false
console.log(9 < 10); //true

//Comparação de tipos === valor e tipo igual a
console.log(10 === 10)//10 ele ´do tipo Number! (true)
console.log(10 === 'Danie') // 10 => number | Danie => String (false)